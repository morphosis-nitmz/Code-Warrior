#include<stdio.h>
int main()
{
	long long int n,i,j=0,sum=0,a[100001],b[100001],c=0,t;
	scanf("%I64d",&n);
	for(i=0;i<n;++i)
	{
		scanf("%d",&a[i]);
		sum+=a[i];
		if(a[i]%2==0)
		{
			++c;
			b[j]=a[i];
			++j;
		}
	}
	if(c<=n/2)
	{
		for(i=0;i<c;++i)
		sum+=b[i];
	}
	else
	{
		for(i=1;i<n;++i)
		for(j=0;j<n-i;++j)
		{
			if(b[j]>b[j+1])
			{
				t=b[j];
				b[j]=b[j+1];
				b[j+1]=t;
			}
		}
		for(i=0;i<n/2;++i)
		sum+=b[i];
	}
	printf("%I64d",sum);
	return 0;
}
